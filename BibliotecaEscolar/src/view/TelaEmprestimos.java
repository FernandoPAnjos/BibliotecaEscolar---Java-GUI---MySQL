    private void btnEmprestarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEmprestarActionPerformed
      try {
        Livro livroSel = (Livro) cbLivros.getSelectedItem();
        Membro membroSel = (Membro) cbMembros.getSelectedItem();
        
        controller.registrarEmprestimo(livroSel, membroSel);
        
        JOptionPane.showMessageDialog(this, "Empréstimo realizado");
        listarEmprestimos(); //Atualizar a lista para remover o livro que foi empestado
        
        carregarCombos(); // Atualizar a ComboBox para remover o livro que foi emprestado
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
    } // TODO add your handling code here:
    }//GEN-LAST:event_btnEmprestarActionPerformed

    private void btnDevolverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDevolverActionPerformed
     if (tblEmprestimos.getSelectedRow() != -1) {
        int id = (int) tblEmprestimos.getValueAt(tblEmprestimos.getSelectedRow(), 0);
        
        controller.devolverLivro(id);
        
        JOptionPane.showMessageDialog(this, "Livro devolvido");
        listarEmprestimos(); //Atualizar a lista para remover o livro que foi empestado
        
        carregarCombos(); // Atualizar a ComboBox para remover o livro que foi emprestado
        
    } else {
        JOptionPane.showMessageDialog(this, "Selecione um empréstimo para encerrar");
    }   // TODO add your handling code here:
    }//GEN-LAST:event_btnDevolverActionPerformed


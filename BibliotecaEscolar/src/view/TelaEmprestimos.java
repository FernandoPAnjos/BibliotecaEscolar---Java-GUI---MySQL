    private void carregarCombos() {
        cbLivros.removeAllItems();
        cbMembros.removeAllItems();
        
        // Graças ao método toString() que fizemos nos Models, aqui aparecerá o nome!
        for (Livro l : controller.listarLivrosParaCombo()) {
            cbLivros.addItem(l); 
        }
        for (Membro m : controller.listarMembrosParaCombo()) {
            cbMembros.addItem(m);
        }
    }
    
    private void listarEmprestimos() {
        DefaultTableModel modelo = (DefaultTableModel) tblEmprestimos.getModel();
        modelo.setNumRows(0);
        
        for (Emprestimo e : controller.listarEmprestimos()) {
            modelo.addRow(new Object[]{
                e.getId(),
                e.getLivro().getTitulo(), // Pega o título dentro do objeto livro
                e.getMembro().getNome(),  // Pega o nome dentro do objeto membro
                e.getDataInicio(),
                (e.getDataFim() == null ? "Ainda sem devolução" : e.getDataFim())
            });
        }
    }


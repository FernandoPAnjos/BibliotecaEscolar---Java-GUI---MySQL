    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
      try {
            controller.cadastrarMembro(txtNome.getText(), txtMatricula.getText(), txtContato.getText());
            JOptionPane.showMessageDialog(this, "Membro cadastrado!");
            listarMembros();
            limparCampos();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
        }  // TODO add your handling code here:
    }//GEN-LAST:event_btnCadastrarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (tblMembros.getSelectedRow() != -1) {
            try {
                int id = (int) tblMembros.getValueAt(tblMembros.getSelectedRow(), 0);
                controller.alterarMembro(id, txtNome.getText(), txtMatricula.getText(), txtContato.getText());
                JOptionPane.showMessageDialog(this, "Membro atualizado!");
                listarMembros();
                limparCampos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro: " + e.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um membro.");
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeletarActionPerformed
      if (tblMembros.getSelectedRow() != -1) {
            int id = (int) tblMembros.getValueAt(tblMembros.getSelectedRow(), 0);
            int opt = JOptionPane.showConfirmDialog(this, "Excluir membro?", "Confirmação", JOptionPane.YES_NO_OPTION);
            if(opt == JOptionPane.YES_OPTION){
                controller.excluirMembro(id);
                listarMembros();
                limparCampos();
            }
        }  // TODO add your handling code here:
    }//GEN-LAST:event_btnDeletarActionPerformed

    private void tblMembrosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMembrosMouseClicked
      if (tblMembros.getSelectedRow() != -1) {
            txtNome.setText(tblMembros.getValueAt(tblMembros.getSelectedRow(), 1).toString());
            txtMatricula.setText(tblMembros.getValueAt(tblMembros.getSelectedRow(), 2).toString());
            txtContato.setText(tblMembros.getValueAt(tblMembros.getSelectedRow(), 3).toString());
        }  // TODO add your handling code here:
    }//GEN-LAST:event_tblMembrosMouseClicked


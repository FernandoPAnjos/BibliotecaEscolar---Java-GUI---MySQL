    private void listarMembros() {
        DefaultTableModel modelo = (DefaultTableModel) tblMembros.getModel();
        modelo.setNumRows(0);
        for (Membro m : controller.listarMembros()) {
            modelo.addRow(new Object[]{
                m.getId(),
                m.getNome(),
                m.getMatricula(),
                m.getContato()
            });
        }
    }
    
